public class Message
{
    private String msg = "";
    private String name = "";
    public String getMsg()
    {
        return msg;
    }
    public String getName()
    {
        return name;
    }
    public void setMsg(String str)
    {
        msg = str;
    }
    public void setName(String str)
    {
        name = str;
    }
}
